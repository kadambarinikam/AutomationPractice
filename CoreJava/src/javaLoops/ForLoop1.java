package javaLoops;

public class ForLoop1 {

	public static void main(String[] args) {
		//Print all the alphabets between A-Z
		//Syntax:for(declaration;condition;operation)
		//    System.out.println()
		
		for(char ch = 'A' ; ch <= 'Z' ;ch++) {
			System.out.println(ch);
		}

		System.out.println("***********");
		//print all a-z alphabets
		
		for(char ch = 'a'; ch <= 'z'; ch++) {
			System.out.println(ch);
	}
	}

}
